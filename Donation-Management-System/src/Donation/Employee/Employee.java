package Donation.Employee;

/**
 *
 * @author Raushan
 */
public class Employee {
    
    private int id;
    private String name;

    public Employee(int size) {
        id = size;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    @Override
    public String toString() {
        return this.name;
    }
    
}
